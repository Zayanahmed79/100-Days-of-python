tuple = (1, 3 ,5 , 7, 9, 3, 5, 7, 9)

tuple2 = tuple.count(3)  # Count occurrences of 3 in the tuple
print("Count of 3 in tuple:", tuple2)

tuple3 = tuple.index(5 )  # Find the index of the first occurrence of 5
print("Index of first occurrence of 5 in tuple:", tuple3)

tuple4 = tuple.index(5, 3)  # Find the index of the first occurrence of 5 starting from index 3
print("Index of first occurrence of 5 starting from index 3:", tuple4)



